import os
import json
from datetime import datetime
from typing import Dict, Any, Optional, List
from dataclasses import dataclass
from dotenv import load_dotenv

try:
    from simple_salesforce import Salesforce
    from simple_salesforce.exceptions import SalesforceError
except ImportError:
    print("‚ö†Ô∏è simple-salesforce n√£o encontrado. Instale com: pip install simple-salesforce")
    Salesforce = None
    SalesforceError = Exception


@dataclass 
class SalesforceConnection:
    """Configura√ß√£o de conex√£o Salesforce."""
    username: str
    password: str
    security_token: str
    domain: str = 'login'  # 'login' para produ√ß√£o, 'test' para sandbox


class SalesforceIntegrator:
    """Integrador oficial Salesforce API."""
    
    def __init__(self, config: Optional[SalesforceConnection] = None):
        """Inicializar integrador Salesforce."""
        load_dotenv()
        
        self.config = config or self._load_config_from_env()
        self.sf = None
        self.connected = False
        
        # Conectar automaticamente ao Salesforce
        self.connect()
        
    def _load_config_from_env(self) -> SalesforceConnection:
        """Carregar configura√ß√£o do arquivo .env."""
        return SalesforceConnection(
            username=os.getenv('SALESFORCE_USERNAME', ''),
            password=os.getenv('SALESFORCE_PASSWORD', ''),
            security_token=os.getenv('SALESFORCE_SECURITY_TOKEN', ''),
            domain=os.getenv('SALESFORCE_DOMAIN', 'login')
        )
    
    def connect(self) -> bool:
        """Conectar ao Salesforce."""
        if not Salesforce:
            print("‚ùå simple-salesforce n√£o est√° instalado!")
            return False
            
        try:
            print("üîó Conectando ao Salesforce...")
            
            # Validar credenciais
            if not all([self.config.username, self.config.password, self.config.security_token]):
                print("‚ùå Credenciais Salesforce incompletas no .env!")
                print("üìù Configure: SALESFORCE_USERNAME, SALESFORCE_PASSWORD, SALESFORCE_SECURITY_TOKEN")
                return False
            
            # Conectar
            self.sf = Salesforce(
                username=self.config.username,
                password=self.config.password,
                security_token=self.config.security_token,
                domain=self.config.domain
            )
            
            # Testar conex√£o
            user_info = self.sf.query("SELECT Id, Name FROM User WHERE Username = '{}'".format(self.config.username))
            
            if user_info['totalSize'] > 0:
                user_name = user_info['records'][0]['Name']
                print(f"‚úÖ Conectado como: {user_name}")
                self.connected = True
                return True
            else:
                print("‚ùå Usu√°rio n√£o encontrado!")
                return False
                
        except SalesforceError as e:
            print(f"‚ùå Erro Salesforce: {str(e)}")
            return False
        except Exception as e:
            print(f"‚ùå Erro de conex√£o: {str(e)}")
            return False
    
    def lookup_account_by_phone(self, phone: str) -> Optional[Dict[str, Any]]:
        """Buscar Account pelo telefone via Contact.
        
        Se encontrar um Contact com o telefone, retorna a Account vinculada.
        """
        if not self.connected:
            print("‚ùå N√£o conectado ao Salesforce!")
            return None
        
        try:
            # Normalizar telefone para a busca
            phone_clean = phone.replace('+', '').replace('-', '').replace(' ', '').replace('(', '').replace(')', '')
            
            # Primeiro procurar Contact com esse telefone
            query = f"""
                SELECT Id, FirstName, LastName, Phone, AccountId, Account.Name, Account.Apelido__c
                FROM Contact 
                WHERE Phone LIKE '%{phone_clean}%' 
                LIMIT 1
            """
            
            result = self.sf.query(query)
            
            if result['totalSize'] > 0:
                contact = result['records'][0]
                account = contact.get('Account')
                
                if account:
                    account_id = contact.get('AccountId')
                    account_name = account.get('Name')
                    apelido = account.get('Apelido__c')
                    
                    print(f"‚úÖ Contact encontrado: {contact.get('FirstName')} {contact.get('LastName')}")
                    print(f"   Account: {account_name} (ID: {account_id})")
                    print(f"   Apelido__c: {apelido}")
                    
                    return {
                        'Id': account_id,
                        'Name': account_name,
                        'Apelido__c': apelido,
                        'Contact': {
                            'Id': contact.get('Id'),
                            'FirstName': contact.get('FirstName'),
                            'LastName': contact.get('LastName')
                        }
                    }
            
            # Se n√£o encontrou Contact, procurar Account direto
            print(f"‚ö†Ô∏è Nenhum Contact encontrado com telefone: {phone} - tentando Account direto")
            
            query_account = f"""
                SELECT Id, Name, Phone, Apelido__c 
                FROM Account 
                WHERE Phone LIKE '%{phone_clean}%' 
                LIMIT 1
            """
            
            result_account = self.sf.query(query_account)
            if result_account['totalSize'] > 0:
                account = result_account['records'][0]
                print(f"‚úÖ Account encontrado: {account.get('Name')}")
                print(f"   Apelido__c: {account.get('Apelido__c')}")
                return account
            
            print(f"‚ö†Ô∏è Nenhuma Account ou Contact encontrada com telefone: {phone}")
            return None
                
        except Exception as e:
            print(f"‚ö†Ô∏è Erro ao procurar Account/Contact: {e}")
            return None
    
    def create_lead(self, data: Dict[str, Any], source_phone: Optional[str] = None) -> Optional[str]:
        """Criar Lead no Salesforce.
        
        Se source_phone for fornecido, procura Account/Contact e vincula o Lead.
        Remove campos que o usu√°rio n√£o tem permiss√£o de escrever.
        """
        if not self.connected:
            print("‚ùå N√£o conectado ao Salesforce!")
            return None
        
        # Se temos source_phone, procurar Account e extrair dados
        if source_phone:
            account_info = self.lookup_account_by_phone(source_phone)
            if account_info:
                # Adicionar AccountId ao Lead para vincula√ß√£o
                data['AccountId'] = account_info.get('Id')
                print(f"‚úÖ Lead ser√° vinculado √† Account: {account_info.get('Name')}")
                
                # Se tem Apelido__c, adicionar tamb√©m
                if account_info.get('Apelido__c'):
                    data['Apelido__c'] = account_info.get('Apelido__c')
                    print(f"‚úÖ Adicionado Apelido__c ao Lead: {account_info.get('Apelido__c')}")
        
        # Remover campos que o usu√°rio n√£o tem permiss√£o de escrever
        # Estes campos ser√£o preenchidos pelos triggers do Salesforce
        readonly_fields = [
            'Company',  # Ser√° preenchido por trigger
            'EmFila__c',  # Ser√° preenchido por trigger
            'Qualificado_para_negociacao__c',
            'VendedorF__c',
            'Supervisor_F__c',
            'GestaoF__c',
            'ReatribuirFila__c',
            'MostrarChatBeetalk__c',
            'CelularDivergente__c',
            'EmailDivergente__c',
            'VisitaConfirmada__c',
            'ReagendamentoConfirmado__c',
            'SDR_F__c',
            'Celular_Pendente__c'
        ]
        
        data_clean = {k: v for k, v in data.items() if k not in readonly_fields}
        
        try:
            result = self.sf.Lead.create(data_clean)
            lead_id = result['id']
            print(f"‚úÖ Lead criado: {lead_id}")
            return lead_id
        except Exception as e:
            error_str = str(e)
            
            # Verificar se o erro foi no trigger AfterUpdate (Lead foi criado, mas trigger falhou)
            if "AfterUpdate" in error_str and "00Q89" in error_str:
                # Extrair o ID do Lead do erro
                import re
                match = re.search(r"id (00Q\w+)", error_str)
                if match:
                    lead_id = match.group(1)
                    print(f"‚ö†Ô∏è  Lead criado apesar do erro de trigger: {lead_id}")
                    print(f"   Erro do trigger: Campos customizados n√£o encontrados")
                    print(f"   Lead foi salvo no Salesforce")
                    return lead_id
            
            print(f"‚ùå Erro ao criar Lead: {error_str}")
            return None
    
    def search_lead_by_name(self, first_name: Optional[str] = None, last_name: Optional[str] = None) -> Optional[List[Dict[str, Any]]]:
        """Buscar Leads pelo nome (FirstName e/ou LastName)."""
        if not self.connected:
            print("‚ùå N√£o conectado ao Salesforce!")
            return None
        
        try:
            if not first_name and not last_name:
                print("‚ùå Precisa fornecer FirstName e/ou LastName!")
                return None
            
            # Construir query dinamicamente
            where_clauses = []
            if first_name:
                where_clauses.append(f"FirstName LIKE '%{first_name}%'")
            if last_name:
                where_clauses.append(f"LastName LIKE '%{last_name}%'")
            
            where_clause = " OR ".join(where_clauses) if where_clauses else ""
            
            query = f"""
                SELECT Id, FirstName, LastName, Phone, Email, Status, Company, IsConverted
                FROM Lead
                WHERE {where_clause}
                ORDER BY CreatedDate DESC
                LIMIT 10
            """
            
            result = self.sf.query(query)
            
            if result['totalSize'] > 0:
                print(f"‚úÖ {result['totalSize']} Lead(s) encontrado(s):")
                for lead in result['records']:
                    status = " [CONVERTIDO]" if lead.get('IsConverted') else ""
                    print(f"   ‚Ä¢ {lead.get('FirstName', '')} {lead.get('LastName', '')} (ID: {lead['Id']}){status}")
                return result['records']
            else:
                print(f"‚ö†Ô∏è Nenhum Lead encontrado com esse nome")
                return None
                
        except Exception as e:
            print(f"‚ùå Erro ao buscar Lead: {e}")
            return None
    
